    private void loadData(User user) throws IOException{
    this.usr = user;
       lbFullName.setText(user.getName());
      List<Inbox> inbx = api.getInbox(user.getEmail());
        
        for (int i = 0; i < inbx.size(); i++) {
            switch (i) {
                case 0:
                    lbHeader0.setText(inbx.get(0).getHeader());
                    lbDate0.setText(inbx.get(0).getDate());
                    lbBody0.setText(inbx.get(0).getBody());
                    pnInbox1.setVisible(false);
                    pnInbox2.setVisible(false);
                    pnInbox3.setVisible(false);
                    break;
                case 1:
                    lbHeader1.setText(inbx.get(1).getHeader());
                    lbDate1.setText(inbx.get(1).getDate());
                    lbBody1.setText(inbx.get(1).getBody());
                    pnInbox1.setVisible(true);
                    pnInbox2.setVisible(false);
                    pnInbox3.setVisible(false);
                    break;
                case 2:
                    lbHeader2.setText(inbx.get(2).getHeader());
                    lbDate2.setText(inbx.get(2).getDate());
                    lbBody2.setText(inbx.get(2).getBody());
                    pnInbox2.setVisible(true);
                    pnInbox3.setVisible(false);
                    break;
                case 3:
                    lbHeader3.setText(inbx.get(3).getHeader());
                    lbDate3.setText(inbx.get(3).getDate());
                    lbBody3.setText(inbx.get(3).getBody());
                    pnInbox3.setVisible(true);
                    break;

            }
        }
    }

